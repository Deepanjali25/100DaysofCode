public class parent {
    protected static int a = 2;
    protected static int b = 4;
    protected static int c = 7;
    protected static int sum(int a, int b, int c) {
        int sum = a + b + c;
        return sum;
    }
    protected static int diff(int a, int b, int c) {
        int diff = a - b - c;
        return diff;
    }
    protected static int multiplication(int a, int b, int c) {
        int multiplication = a * b * c;
        return multiplication;
    }
}
